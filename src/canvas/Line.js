
class SolidLine {
  constructor({ draw, color }) {
    this.draw = draw;
    this.color = color;
  }
}

class DottedLine {
  constructor({ draw, color }) {
    this.draw = draw;
    this.color = color;
  }
}

class DoubleLine {
  constructor({ draw, color }) {
    this.draw = draw;
    this.color = color;
  }
}

export {
  SolidLine, DottedLine, DoubleLine,
};
